    public static void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) JT_lista.getModel();
        modelo.setNumRows(0);

        ProdutoDAO dao = new ProdutoDAO();
                
        for (Produto c: dao.findAll()) {
                     
            modelo.addRow(new Object[]{ c.getId_prod(),
                c.getCodint_prod(), 
                c.getDes_prod(), 
                c.getMol_prod(),});
        }
    }

    public void readId(String cod) {
        DefaultTableModel modelo1 = (DefaultTableModel) JT_lista.getModel();
        modelo1.setNumRows(0);

        MoldeDAO dao = new MoldeDAO();
        for (Molde c : dao.readId(cod)) {
            modelo1.addRow(new Object[]{
            c.getDes_mol(),});
        }
    }

    public void readRef(String cod) {
        DefaultTableModel modelo = (DefaultTableModel) JT_lista.getModel();
        modelo.setNumRows(0);

        ProdutoDAO dao = new ProdutoDAO();
        for (Produto c : dao.readCod(cod)) {
            modelo.addRow(new Object[]{
                c.getId_prod(),
                c.getCodint_prod(),
                c.getMol_prod(),});
        }
    }

    public void readDes(String cod) {
        DefaultTableModel modelo = (DefaultTableModel) JT_lista.getModel();
        modelo.setNumRows(0);

        ProdutoDAO dao = new ProdutoDAO();
        for (Produto c : dao.readDes(cod)) {
            modelo.addRow(new Object[]{
                c.getId_prod(),
                c.getCodint_prod(),
                c.getMol_prod(),});
        }
    }

