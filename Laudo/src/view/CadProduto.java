    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        // TODO add your handling code here:
        if (JT_lista.getSelectedRow() != -1) {
            Produto c = new Produto();
            ProdutoDAO dao = new ProdutoDAO();

            int excluir = JOptionPane.showConfirmDialog(null, "Deseja excluir: " + JT_lista.getValueAt(JT_lista.getSelectedRow(), 1));
            if (excluir == JOptionPane.YES_OPTION) {
                c.setId_prod((int) JT_lista.getValueAt(JT_lista.getSelectedRow(), 0));
                dao.delete(c);
                readTable();
                
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um molde");
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

