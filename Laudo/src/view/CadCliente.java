    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        // TODO add your handling code here:
        if (JT_cliente.getSelectedRow() != -1) {
            Cliente c = new Cliente();
            ClienteDAO dao = new ClienteDAO();

            int excluir = JOptionPane.showConfirmDialog(null, "Deseja excluir: " + JT_cliente.getValueAt(JT_cliente.getSelectedRow(), 1));
            if (excluir == JOptionPane.YES_OPTION) {
                c.setId_cli((int) JT_cliente.getValueAt(JT_cliente.getSelectedRow(), 0));
                dao.delete(c);
                readTable();
                txt_nome.setText("");
                txt_cnpj.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

