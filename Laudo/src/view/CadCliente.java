    public static void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) JT_cliente.getModel();
        modelo.setNumRows(0);

        ClienteDAO dao = new ClienteDAO();
        for (Cliente c : dao.findAll()) {
            modelo.addRow(new Object[]{
                c.getId_cli(),
                c.getNome_cli(),
                c.getCnpj_cli(),});
        }
    }

    public void readNome(String cod) {
        DefaultTableModel modelo = (DefaultTableModel) JT_cliente.getModel();
        modelo.setNumRows(0);

        ClienteDAO dao = new ClienteDAO();
        for (Cliente c : dao.readNome(cod)) {
            modelo.addRow(new Object[]{
                c.getId_cli(),
                c.getNome_cli(),
                c.getCnpj_cli(),});
        }
    }

    public void readCnpj(String cod) {
        DefaultTableModel modelo = (DefaultTableModel) JT_cliente.getModel();
        modelo.setNumRows(0);

        ClienteDAO dao = new ClienteDAO();
        for (Cliente c : dao.readCnpj(cod)) {
            modelo.addRow(new Object[]{
                c.getId_cli(),
                c.getNome_cli(),
                c.getCnpj_cli(),});
        }
    }

