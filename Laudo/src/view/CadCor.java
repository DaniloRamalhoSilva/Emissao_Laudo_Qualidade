    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tb_cor.getModel();
        modelo.setNumRows(0);

        CoresDAO cdao = new CoresDAO();
        for (Cores c : cdao.findAll()) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCod_cor(),
                c.getDes_cor(),});
        }
    }

    public void readTableForCod(String cod) {
        DefaultTableModel modelo = (DefaultTableModel) tb_cor.getModel();
        modelo.setNumRows(0);

        CoresDAO cdao = new CoresDAO();
        for (Cores c : cdao.readcod(cod)) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCod_cor(),
                c.getDes_cor(),});
        }
    }
    
    public void readTableForDes(String cod) {
        DefaultTableModel modelo = (DefaultTableModel) tb_cor.getModel();
        modelo.setNumRows(0);

        CoresDAO cdao = new CoresDAO();
        for (Cores c : cdao.readdes(cod)) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCod_cor(),
                c.getDes_cor(),});
        }
    }

    public void salvar() {
        Cores c = new Cores();
        CoresDAO dao = new CoresDAO();

        c.setCod_cor(txt_cod.getText());
        c.setDes_cor(txt_descricao.getText());
        c.setUs_cor(Principal.txt_stat_us.getText());

        dao.save(c);
        readTable();
    }

    public void edite() {
        Cores c = new Cores();
        CoresDAO dao = new CoresDAO();

        c.setCod_cor(txt_cod.getText());
        c.setDes_cor(txt_descricao.getText());
        c.setId((int) tb_cor.getValueAt(tb_cor.getSelectedRow(), 0));
        c.setUs_cor(Principal.txt_stat_us.getText());
        dao.updata(c);
        readTable();
    }

