    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        // TODO add your handling code here:
        if (tb_cor.getSelectedRow() != -1) {

            txt_cod.setText((String) tb_cor.getValueAt(tb_cor.getSelectedRow(), 1));
            txt_descricao.setText((String) tb_cor.getValueAt(tb_cor.getSelectedRow(), 2));

            tb_cor.setEnabled(false);
            bt_salvar.setVisible(true);
            bt_cancela.setVisible(true);
            txt_cod.setEditable(true);
            txt_descricao.setEditable(true);
            bt_Excluir.setEnabled(false);
            bt_novo.setEnabled(false);
            bt_editar.setEnabled(false);
            bt_peq.setEnabled(false);
            bt_conCod.setVisible(false);
            bt_conDescri.setVisible(false);
            k = 2;

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma cor");
        }

    }//GEN-LAST:event_bt_editarActionPerformed

    private void bt_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_ExcluirActionPerformed
        // TODO add your handling code here:
        txt_cod.setText("");
        txt_descricao.setText("");
        if (tb_cor.getSelectedRow() != -1) {

            Cores c = new Cores();
            CoresDAO dao = new CoresDAO();

            int excluir = JOptionPane.showConfirmDialog(null, "Deseja excluir: " + (String) tb_cor.getValueAt(tb_cor.getSelectedRow(), 1));
            if (excluir == JOptionPane.YES_OPTION) {
                c.setId((int) tb_cor.getValueAt(tb_cor.getSelectedRow(), 0));
                dao.delete(c);
                readTable();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma cor");
        }
    }//GEN-LAST:event_bt_ExcluirActionPerformed

    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
        // TODO add your handling code here:

        if (txt_cod.getText().equals("") | txt_descricao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Prencha todos os campos!");
        } else {
            switch (k) {
                case 1:
                    salvar();
                    txt_cod.setText("");
                    txt_descricao.setText("");
                    txt_cod.setEditable(false);
                    txt_descricao.setEditable(false);
                    bt_salvar.setVisible(false);
                    bt_cancela.setVisible(false);
                    bt_Excluir.setEnabled(true);
                    bt_novo.setEnabled(true);
                    bt_editar.setEnabled(true);
                    tb_cor.setEnabled(true);
                    bt_peq.setEnabled(true);

                    break;
                case 2:
                    edite();
                    txt_cod.setText("");
                    txt_descricao.setText("");
                    txt_cod.setEditable(false);
                    txt_descricao.setEditable(false);
                    bt_salvar.setVisible(false);
                    bt_cancela.setVisible(false);
                    bt_Excluir.setEnabled(true);
                    bt_novo.setEnabled(true);
                    bt_editar.setEnabled(true);
                    tb_cor.setEnabled(true);
                    bt_peq.setEnabled(true);

                    break;
                default:
                    System.err.println("Erro:");
            }
        }

    }//GEN-LAST:event_bt_salvarActionPerformed

